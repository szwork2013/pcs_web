import cookie from 'react-cookie'

/**
 * cookie
 * 新增cookie
 * @export
 * @param {any} key
 * @param {any} vlu
 * @param {any} option
 */
export function save (key, vlu, option) {
	cookie.save(key, vlu, option)
}

/**
 * cookie
 * 删除cookie
 * @export
 * @param {any} key
 * @param {any} option
 */
export function remove (key, option) {
	cookie.remove(key, option)
}


/**
 * cookie
 * 获取cookie
 * @export
 * @param {any} key
 */
export function load (key) {
	cookie.load(key)
}